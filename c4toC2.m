function C = c4toC2(c)
%% Transfer cijkl to Cij
%% Input: cijkl
%% Output: Cij


C = zeros(6,6);

C(1,2) = c(1,1,2,2);
C(1,3) = c(1,1,3,3);
C(1,4) = c(1,1,2,3);
C(1,5) = c(1,1,1,3);
C(1,6) = c(1,1,1,2);
C(2,3) = c(2,2,3,3);
C(2,4) = c(2,2,2,3);
C(2,5) = c(2,2,1,3);
C(2,6) = c(2,2,1,2);
C(3,4) = c(3,3,2,3);
C(3,5) = c(3,3,1,3);
C(3,6) = c(3,3,1,2);
C(4,5) = c(2,3,1,3);
C(4,6) = c(2,3,1,2);
C(5,6) = c(1,3,1,2);

D = transpose(C);
C = C+D;

C(1,1) = c(1,1,1,1);
C(2,2) = c(2,2,2,2);
C(3,3) = c(3,3,3,3);
C(4,4) = c(2,3,2,3);
C(5,5) = c(1,3,1,3);
C(6,6) = c(1,2,1,2);


end